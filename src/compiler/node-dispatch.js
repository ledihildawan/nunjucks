export const compileDispatch = (ctx, node, frame) => {
  const fn = COMPILE_FUNCTIONS[node.typename];
  if (fn) {
    return fn(ctx, node, frame);
  } else {
    ctx.fail(`compile: Cannot compile node: ${node.typename}`, node.lineno, node.colno);
  }
};
